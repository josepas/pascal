#include<iostream>
using namespace std;

    long int factorial(int n) {
	if (n <= 0) {
	    return 1;
	}
	while (n != 1) {
	    return n * factorial(n-1);
	}
	return 1;
    }
    
    int main() {
	int n;
	long int h;
	cout << "Ingrese el factorial a calcular: ";
	cin >> n;
	h = factorial(n);
	cout << n << "! = " << h << "\n";
	return 0;
    }